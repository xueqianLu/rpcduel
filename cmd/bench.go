package cmd

import (
	"context"
	"fmt"
	"log/slog"
	"math/rand"
	"os"
	"time"

	"github.com/spf13/cobra"
	"github.com/xueqianLu/rpcduel/internal/bench"
	"github.com/xueqianLu/rpcduel/internal/benchgen"
	"github.com/xueqianLu/rpcduel/internal/metrics"
	"github.com/xueqianLu/rpcduel/internal/report"
	"github.com/xueqianLu/rpcduel/internal/rpc"
	"github.com/xueqianLu/rpcduel/internal/runner"
)

var benchCmd = &cobra.Command{
	Use:   "bench",
	Short: "Benchmark one or more RPC endpoints",
	Long: `Send concurrent JSON-RPC requests to one or more endpoints and collect
performance metrics: QPS, average latency, P95, P99, error rate.

When --input is provided, requests are loaded from a bench.json scenario file
(generated by rpcduel benchgen) and --method / --params are ignored.`,
	RunE: runBench,
}

var (
	benchRPCs        []string
	benchMethod      string
	benchParamsStr   string
	benchConcurrency int
	benchRequests    int
	benchDuration    time.Duration
	benchTimeout     time.Duration
	benchOutput      string
	benchInputFile   string
	benchWarmup      time.Duration
	benchHDROut      string
)

func init() {
	benchCmd.Flags().StringArrayVar(&benchRPCs, "rpc", nil, "RPC endpoint URL (can be specified multiple times)")
	benchCmd.Flags().StringVar(&benchMethod, "method", "eth_blockNumber", "JSON-RPC method name")
	benchCmd.Flags().StringVar(&benchParamsStr, "params", "[]", "JSON-encoded params array")
	benchCmd.Flags().StringVar(&benchInputFile, "input", "", "Bench scenario file (from rpcduel benchgen); overrides --method/--params")
	benchCmd.Flags().IntVar(&benchConcurrency, "concurrency", 10, "Number of concurrent workers")
	benchCmd.Flags().IntVar(&benchRequests, "requests", 100, "Total number of requests (0 = use duration)")
	benchCmd.Flags().DurationVar(&benchDuration, "duration", 0, "Run for this long instead of fixed request count (e.g. 30s)")
	benchCmd.Flags().DurationVar(&benchTimeout, "timeout", 30*time.Second, "Per-request timeout")
	benchCmd.Flags().StringVar(&benchOutput, "output", "text", "Output format: text or json")
	benchCmd.Flags().DurationVar(&benchWarmup, "warmup", 0, "Discard results from the first N (e.g. 5s) of the run, before measurement begins")
	benchCmd.Flags().StringVar(&benchHDROut, "hdr-out", "", "Write the HDR percentile distribution log to this file (one file per endpoint, .endpointN suffix)")
}

// scenarioLabel returns tag if non-empty, otherwise the fallback (e.g. method
// name). Used as the "scenario" label for Prometheus metrics.
func scenarioLabel(tag, fallback string) string {
	if tag != "" {
		return tag
	}
	return fallback
}

func runBench(cmd *cobra.Command, args []string) error {
	if err := validateOutputFormat(benchOutput); err != nil {
		return err
	}
	if len(benchRPCs) == 0 {
		return fmt.Errorf("at least one --rpc endpoint is required")
	}

	ctx := runnerContext(context.Background())
	outFmt := report.Format(benchOutput)

	startTime := time.Now()
	warmupEnd := startTime.Add(benchWarmup)
	if benchWarmup > 0 {
		slog.Info("warmup phase started", "duration", benchWarmup)
	}

	// One metrics collector per endpoint, anchored at warmupEnd so that
	// QPS reflects only the measurement window.
	metricsMap := make(map[string]*bench.Metrics)
	for _, ep := range benchRPCs {
		metricsMap[ep] = bench.NewMetricsAt(ep, warmupEnd)
	}

	record := func(res runner.Result, fallbackScenario string) {
		if benchWarmup > 0 && res.Timestamp.Before(warmupEnd) {
			return
		}
		m := metricsMap[res.Endpoint]
		if m == nil {
			m = bench.NewMetricsAt(res.Endpoint, warmupEnd)
			metricsMap[res.Endpoint] = m
		}
		m.Record(res.Latency, res.Err != nil)
		metrics.Observe(res.Endpoint, scenarioLabel(res.Tag, fallbackScenario), res.Latency, res.Err != nil)
	}

	if benchInputFile != "" {
		// ----------------------------------------------------------------
		// Scenario-file mode: load requests from bench.json and run them.
		// ----------------------------------------------------------------
		bf, err := benchgen.LoadBenchFile(benchInputFile)
		if err != nil {
			return fmt.Errorf("load bench file: %w", err)
		}

		// When --requests or --duration is specified, use weighted dispatch.
		// Otherwise, run all requests from the file once (flat mode).
		var requests []benchgen.Request
		if benchDuration > 0 {
			if len(bf.FlattenRequests()) == 0 {
				return fmt.Errorf("bench file contains no requests")
			}
			workers := benchConcurrency
			if workers <= 0 {
				workers = 1
			}
			samplers := make([]*benchgen.WeightedSampler, workers)
			for i := range samplers {
				samplers[i] = bf.NewWeightedSampler(rand.New(rand.NewSource(42 + int64(i))))
			}
			resultCh := runner.RunDurationGenerated(ctx, workers, benchDuration, benchTimeout,
				func(workerID, iteration int) runner.Task {
					req := samplers[workerID].Next()
					return runner.Task{
						Endpoint: benchRPCs[(workerID+iteration)%len(benchRPCs)],
						Method:   req.Method,
						Params:   req.Params,
					}
				})
			for res := range resultCh {
				record(res, "")
			}
			requests = nil
		} else if benchRequests > 0 {
			requests = bf.WeightedRequests(benchRequests, nil)
		} else {
			requests = bf.FlattenRequests()
		}

		if benchDuration == 0 && len(requests) == 0 {
			return fmt.Errorf("bench file contains no requests")
		}

		if benchDuration == 0 {
			// Build runner.Task list, round-robining across endpoints.
			tasks := make([]runner.Task, len(requests))
			for i, req := range requests {
				tasks[i] = runner.Task{
					Endpoint: benchRPCs[i%len(benchRPCs)],
					Method:   req.Method,
					Params:   req.Params,
				}
			}
			resultCh := runner.Run(ctx, tasks, benchConcurrency, benchTimeout)
			for res := range resultCh {
				record(res, "")
			}
		}
	} else {
		// ----------------------------------------------------------------
		// Single-method mode (original behavior).
		// ----------------------------------------------------------------
		params, err := rpc.ParseParams(benchParamsStr)
		if err != nil {
			return err
		}

		var resultCh <-chan runner.Result

		if benchDuration > 0 {
			resultCh = runner.RunDuration(ctx, benchRPCs, benchMethod, params,
				benchConcurrency, benchDuration, benchTimeout)
		} else {
			if benchRequests <= 0 {
				benchRequests = 100
			}
			resultCh = runner.RunN(ctx, benchRPCs, benchMethod, params,
				benchConcurrency, benchRequests, benchTimeout)
		}

		for res := range resultCh {
			record(res, benchMethod)
		}
	}

	var summaries []bench.Summary
	for i, ep := range benchRPCs {
		m := metricsMap[ep]
		m.Finish()
		summaries = append(summaries, m.Summarize())
		if benchHDROut != "" {
			fname := fmt.Sprintf("%s.%d.hdr", benchHDROut, i)
			f, err := os.Create(fname)
			if err != nil {
				slog.Warn("hdr-out: open failed", "file", fname, "err", err)
				continue
			}
			if err := m.WriteHDR(f); err != nil {
				slog.Warn("hdr-out: write failed", "file", fname, "err", err)
			}
			_ = f.Close()
			slog.Info("hdr-out written", "file", fname, "endpoint", ep)
		}
	}

	rep := report.BenchReport{Summaries: summaries}
	report.PrintBench(os.Stdout, rep, outFmt)
	return nil
}
