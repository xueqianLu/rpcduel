package cmd

import (
	"context"
	"fmt"
	"os"
	"time"

	"github.com/spf13/cobra"
	"github.com/xueqianLu/rpcduel/internal/bench"
	"github.com/xueqianLu/rpcduel/internal/benchgen"
	"github.com/xueqianLu/rpcduel/internal/report"
	"github.com/xueqianLu/rpcduel/internal/rpc"
	"github.com/xueqianLu/rpcduel/internal/runner"
)

var benchCmd = &cobra.Command{
	Use:   "bench",
	Short: "Benchmark one or more RPC endpoints",
	Long: `Send concurrent JSON-RPC requests to one or more endpoints and collect
performance metrics: QPS, average latency, P95, P99, error rate.

When --input is provided, requests are loaded from a bench.json scenario file
(generated by rpcduel benchgen) and --method / --params are ignored.`,
	RunE: runBench,
}

var (
	benchRPCs        []string
	benchMethod      string
	benchParamsStr   string
	benchConcurrency int
	benchRequests    int
	benchDuration    time.Duration
	benchTimeout     time.Duration
	benchOutput      string
	benchInputFile   string
)

func init() {
	benchCmd.Flags().StringArrayVar(&benchRPCs, "rpc", nil, "RPC endpoint URL (can be specified multiple times)")
	benchCmd.Flags().StringVar(&benchMethod, "method", "eth_blockNumber", "JSON-RPC method name")
	benchCmd.Flags().StringVar(&benchParamsStr, "params", "[]", "JSON-encoded params array")
	benchCmd.Flags().StringVar(&benchInputFile, "input", "", "Bench scenario file (from rpcduel benchgen); overrides --method/--params")
	benchCmd.Flags().IntVar(&benchConcurrency, "concurrency", 10, "Number of concurrent workers")
	benchCmd.Flags().IntVar(&benchRequests, "requests", 100, "Total number of requests (0 = use duration)")
	benchCmd.Flags().DurationVar(&benchDuration, "duration", 0, "Run for this long instead of fixed request count (e.g. 30s)")
	benchCmd.Flags().DurationVar(&benchTimeout, "timeout", 30*time.Second, "Per-request timeout")
	benchCmd.Flags().StringVar(&benchOutput, "output", "text", "Output format: text or json")
}

func runBench(cmd *cobra.Command, args []string) error {
	if len(benchRPCs) == 0 {
		return fmt.Errorf("at least one --rpc endpoint is required")
	}

	ctx := context.Background()
	outFmt := report.Format(benchOutput)

	// One metrics collector per endpoint
	metricsMap := make(map[string]*bench.Metrics)
	for _, ep := range benchRPCs {
		metricsMap[ep] = bench.NewMetrics(ep)
	}

	if benchInputFile != "" {
		// ----------------------------------------------------------------
		// Scenario-file mode: load requests from bench.json and run them.
		// ----------------------------------------------------------------
		bf, err := benchgen.LoadBenchFile(benchInputFile)
		if err != nil {
			return fmt.Errorf("load bench file: %w", err)
		}
		requests := bf.FlattenRequests()
		if len(requests) == 0 {
			return fmt.Errorf("bench file contains no requests")
		}

		// Build runner.Task list, round-robining across endpoints.
		tasks := make([]runner.Task, len(requests))
		for i, req := range requests {
			tasks[i] = runner.Task{
				Endpoint: benchRPCs[i%len(benchRPCs)],
				Method:   req.Method,
				Params:   req.Params,
			}
		}

		resultCh := runner.Run(ctx, tasks, benchConcurrency, benchTimeout)
		for res := range resultCh {
			m := metricsMap[res.Endpoint]
			if m == nil {
				m = bench.NewMetrics(res.Endpoint)
				metricsMap[res.Endpoint] = m
			}
			m.Record(res.Latency, res.Err != nil)
		}
	} else {
		// ----------------------------------------------------------------
		// Single-method mode (original behaviour).
		// ----------------------------------------------------------------
		params, err := rpc.ParseParams(benchParamsStr)
		if err != nil {
			return err
		}

		var resultCh <-chan runner.Result

		if benchDuration > 0 {
			resultCh = runner.RunDuration(ctx, benchRPCs, benchMethod, params,
				benchConcurrency, benchDuration, benchTimeout)
		} else {
			if benchRequests <= 0 {
				benchRequests = 100
			}
			resultCh = runner.RunN(ctx, benchRPCs, benchMethod, params,
				benchConcurrency, benchRequests, benchTimeout)
		}

		for res := range resultCh {
			m := metricsMap[res.Endpoint]
			if m == nil {
				m = bench.NewMetrics(res.Endpoint)
				metricsMap[res.Endpoint] = m
			}
			m.Record(res.Latency, res.Err != nil)
		}
	}

	var summaries []bench.Summary
	for _, ep := range benchRPCs {
		m := metricsMap[ep]
		m.Finish()
		summaries = append(summaries, m.Summarize())
	}

	rep := report.BenchReport{Summaries: summaries}
	report.PrintBench(os.Stdout, rep, outFmt)
	return nil
}
